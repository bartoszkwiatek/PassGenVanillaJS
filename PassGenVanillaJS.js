/* Generates password of set length with set qualities: 
numbers, words, special signs, big letters etc. */



var number_of_characters = 20;                                  // number of characters in password
var char_letters = 'abcdefghijklmnopqrstuvwxyz';                // ascii letters
var char_special = '!"#$%&\'()*+,-./:;<=>?@[\\]^_`{|}~';        // ascii special characters
var char_numbers = '0123456789';                                // numbers

function charPool(small_letters, big_letters, special_signs, numbers) {        // function creates pool of characters to generate
    var pool = '';
    
    if (small_letters === true) {
        pool = pool + char_letters;
    } 
    
    if (big_letters === true) {
        pool = pool + char_letters.toUpperCase();
    } 
    
    if (special_signs === true) {
        pool = pool + char_special;
    }
    
    if (numbers === true){
        pool = pool + char_numbers;
    }
    return pool;
}

function randomChar(char_set) {
    var set = '';
    if (typeof char_set === "string") {                                                         // select random character from set
        set = char_set.charAt(Math.floor(Math.random() * char_set.length));
        return set;
    }   else if ( Array.isArray(char_set) ) {
        set = char_set[Math.floor(Math.random() * char_set.length)];
        return set;
    }   else {
        return('Wrong characters set: must be array or string');
    }
}

function capitalizeFirst(stringToModify, howMany) {
    var temp = stringToModify;
    var prefix_string = (temp.substr(0,howMany));
    prefix_string = prefix_string.toUpperCase();
    temp = temp.substr(howMany);
    temp = prefix_string + temp;
    return temp;
}   
