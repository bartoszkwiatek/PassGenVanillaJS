function totallyRandom(how_many_chars, pool) {           // generates string of specified length with selected additional signs
    var temp_string = '';
    for (var i = 0; i < how_many_chars; i++) {
        temp_string += randomChar(pool);                                                // randomChar chooses random character from character pool
    }
    return temp_string;
}

function addNumbers(stringToModify, howMany) {
    var suffix_string = totallyRandom(howMany, charPool(false,false,false,true));
    stringToModify = stringToModify + suffix_string;

    return stringToModify;
}


function bigLettersFirstThenRandomNumbersLast(password_length, prefix, suffix, pool) {          // need a better name - starts password with big letters, ends with numbers
    var temp_password = totallyRandom(password_length-suffix, pool);
 
    temp_password = capitalizeFirst(temp_password,prefix);
    temp_password = addNumbers(temp_password,suffix);
    
    return(temp_password);
}               

function mixed(number_of_characters) {                  //operations on arrays are easier
    var vowels = ['a','e','u','i','o'];                 
    var pool = charPool(true, false, false, false);
    var poolArray = [];
    var mixed_password = '';
    for (i=0; i<pool.length; i++) {                     //making pools of characters into arrat
        poolArray[i] = pool.charAt(i);
    }
    
    for (j=0; j<vowels.length; j++) {                       //deleting vowels from pool
        let index = poolArray.indexOf(vowels[j]);
        poolArray.splice(index,1);
    }
    
    for (k=0; k<number_of_characters; k++) {                //password is created from non-vovel and vowel one after another
        if (k%2===0) {
            mixed_password += randomChar(poolArray);

        } else {
            mixed_password += randomChar(vowels);
        }
    }
    
    return(mixed_password);
    }

